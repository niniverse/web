function initPortfolio() {
    renderPortfolio();
}

function renderPortfolio() {
    const container = document.getElementById('portfolio-grid');
    const items = portfolioItems[currentLang];

    container.innerHTML = items.map(item => `
        <div class="portfolio-item fade-in" data-category="${item.category}">
            <div class="portfolio-image">
                <img src="${item.image}" alt="${item.title}" loading="lazy">
            </div>
            <div class="portfolio-content">
                <h3>${item.title}</h3>
                <p>${item.description}</p>
            </div>
        </div>
    `).join('');

    filterPortfolio(currentFilter);
}

function filterPortfolio(category) {
    currentFilter = category;

    // Update active button
    document.querySelectorAll('.filter-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.filter === category);
    });

    // Show/hide items
    document.querySelectorAll('.portfolio-item').forEach(item => {
        if (category === 'all' || item.dataset.category === category) {
            item.classList.remove('hidden');
        } else {
            item.classList.add('hidden');
        }
    });
}

// ========================================
// Event Listeners
// ========================================
function initEventListeners() {
    // Theme toggle
    document.getElementById('theme-toggle').addEventListener('click', toggleTheme);

    // Language toggle
    document.getElementById('lang-toggle').addEventListener('click', toggleLanguage);

    // Portfolio filters
    document.querySelectorAll('.filter-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            filterPortfolio(btn.dataset.filter);
        });
    });

    // Contact form
    document.querySelector('.contact-form').addEventListener('submit', handleFormSubmit);

    // Smooth scroll
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }
        });
    });
}

